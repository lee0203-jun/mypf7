$(function(){

    $('.sic1 .top_img span').on('click',function(){

        // .top_img가 사라진다,video와 닫기버튼이 나타난다.

        $('.sic1 .top_img').fadeOut();

        $('.sic1 .close').css({'display':'block'});
        

        $('.sic1 .videoWrap video').show();

      

    });// click

    $('.close').on('click',function(){


        // $('.videoWrap video').fadeOut();
        // $('.close').fadeOut();

        $('.sic1 .close').css({'display':'none'});
        $('.sic1 .videoWrap video').css({'display':'none'});

        $('.sic1 .top_img').show();

    })




});// doc

